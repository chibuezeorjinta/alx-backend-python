#!/usr/bin/env python3
"""Concatenate two strings"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate strings.
    :type str2: str.
    :type str1: str.
    :param str1: string 1.
    :param str2: string 2.
    :return: combined string.
    """
    return str1 + str2
